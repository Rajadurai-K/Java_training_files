public class methodride {
    static void add(int a, int b)
    {
        System.out.println("Sum of two numbers : "+(a+b));
    }    
    static void add(String a)
    {
        System.out.println("Name of the person : "+a);
    }    
    public static void main(String[] args)
    {
        add(10,20);
        add("Rajadurai");
    }
}
